import React from 'react';
import { Header } from './components/header';
import { Card, CardContent } from './components/common/card';
import './globals.css';
import { Toaster } from './components/common/toast/toaster';
import UserProvider from '@/lib/contexts/user-context';
import { useUser } from '@/lib/user-auth';

export const metadata = {
  title: 'The-Playmaker',
  description: 'Generated by Next.js',
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const user = await useUser();
  return (
    <html>
      <body>
        <UserProvider username={user?.username} isLoggedIn={user.isLoggedIn}>
          <Header />
          {!user.isLoggedIn && (
            <div className="flex justify-center bg-slate-50">
              <span className="text-sm">
                You are not signed in. Please log in or register
              </span>
            </div>
          )}
          <Toaster />
          <div className="mx-2 sm:px-4 xl:px-2">
            <Card>
              <CardContent>
                <div className="flex justify-center">Announcement banner</div>
              </CardContent>
            </Card>
            <main className="flex h-screen w-full flex-col flex-wrap md:content-center">
              {children}
            </main>
          </div>
        </UserProvider>
      </body>
    </html>
  );
}
